public class Punto {
    
    int coord_X;
    int coord_Y;
     
    public Punto(int coord_X, int coord_Y) {
        this.coord_X = coord_X;
        this.coord_Y = coord_Y;
    }

    public int getCoord_X() {
        return coord_X;
    }

    public void setCoord_X(int coord_X) {
        this.coord_X = coord_X;
    }

    public int getCoord_Y() {
        return coord_Y;
    }

    public void setCoord_Y(int coord_Y) {
        this.coord_Y = coord_Y;
    }
    
}
